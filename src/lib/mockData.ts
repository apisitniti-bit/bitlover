export const calculatePortfolio = () => {
  const totalValue = cryptoData.reduce((sum, coin) => sum + (coin.price * coin.quantity), 0);
  const btcEquivalent = totalValue / cryptoData[0].price;
  const weightedChange = cryptoData.reduce((sum, coin) => {
    const value = coin.price * coin.quantity;
    return sum + (coin.change_24h * (value / totalValue));
  }, 0);
  
  return {
    totalValue,
    btcEquivalent,
    averageChange: weightedChange
  };
};
